package Week1;

public class VaryingVariables {
    public static void main(String[] args) {

        int chickenNumber = 9000;
        double baconNumberDec = 0.1;
        String tractorName = "Zetor";

        System.out.println("Chickens:\n" + chickenNumber);
        System.out.println("Bacon (kg):\n" + baconNumberDec);
        System.out.println("A tractor: \n" + tractorName);
        System.out.println();
        System.out.println("In a nutshell:\n" + chickenNumber + "\n" + baconNumberDec + "\n" + tractorName);
    }
}
